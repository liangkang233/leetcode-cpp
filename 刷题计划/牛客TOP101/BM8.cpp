#include <iostream>
#include <string>
#include <vector>
using namespace std;

struct ListNode {
	int val;
	struct ListNode *next;
	ListNode(int x) :
			val(x), next(NULL) {
	}
};

class Solution {
public:
    /**
     * 代码中的类名、方法名、参数名已经指定，请勿修改，直接返回方法规定的值即可
     *
     * 
     * @param pHead ListNode类 
     * @param k int整型 
     * @return ListNode类
     */
    ListNode* FindKthToTail(ListNode* pHead, int k) {
        ListNode* head = pHead, *tail = head;
		for (int i = 0; i < k; i++) {
			if(!tail) return nullptr;
			tail = tail->next;
		}
		while (tail) {
			tail = tail->next;
			head = head->next;
		}
		return head;
    }
};