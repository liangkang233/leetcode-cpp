// @lc code=start
class Solution {
public:
    bool isPowerOfTwo(int n) {
        for (long long i = 1; i <= n; i*=2) {
            if (i == n)
                return true;
        }
        return false;
    }
};
// @lc code=end

class Solution {
public:
    bool isPowerOfTwo(int n) {
        if(n==0) return false;
        while(n%2==0) n/=2;
        return n==1;
    }
};


class Solution {
public:
    bool isPowerOfTwo(int n) {
        if(n==0) return false;
        return ((n==1) || (n%2==0 && isPowerOfTwo(n/2)));
    }
};